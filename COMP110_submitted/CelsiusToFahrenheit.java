//Purpose: Convert Celsius To Fahrenheit
class CelsiusToFahrenheit {
   public static void main(String[] args) {   
      // declaration 
      double fahrenheit;
      double celsius;

      // initialization
      celsius = 23.0; 

      // compute area
      fahrenheit = celsius * 1.8 + 32;
      System.out.println(celsius + " degrees celsius is " + fahrenheit + " degrees fahrenheit");
   }
}
